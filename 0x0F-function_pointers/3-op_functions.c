#include "3-calc.h"

/**
 * op_add - add two input numbers
 * @a: first number
 * @b: second number
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtract two input numbers
 * @a: first number
 * @b: second number
 * Return: result
 */


int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply two input numbers
 * @a: first number
 * @b: second number
 * Return: result
 */


int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide two input numbers
 * @a: first number
 * @b: second number
 * Return: result
 */


int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of two input numbers
 * @a: first number
 * @b: second number
 * Return: result
 */


int op_mod(int a, int b)
{
	return (a % b);
}
